         <p style="font-size:20px;" > Zion Health Foundation Hepatitis  Awareness Campaign In Kumba
<h1 style="font-size:20px;">Background and justification:<h1>
Hepatitis, particularly Hepatitis B and C, is a major health concern in the world with  over 1. 5 million new infections.It is worst in Africa, with the highest record of morbidity and mortality. According to a systematic review and meta-analysis of studies conducted between 2000 and 2016, the overall pooled sero-prevalence of hepatitis B virus in Cameroon was estimated to be 11.2% among a total of 105,603 individuals. This means that at least one in every ten persons is infected with Hepatitis B, less than 10% of all who are infected  know they are infected. 
It is spread when a person who is not immunized comes in contact with blood or body fluids from an infected person. 

It is in this background that the Zion Health Foundation conceived a Hepatitis B awareness campaign  since May 2023 (tilted stop hepatitis) , as a first step of an extensive initiative to reverse the current trends and bridge the gaps at the different levels.

<h1 style="font-size:20px;">.Overview:<h1> The Hepatitis B awareness campaign was launched in word of light prayer ministry( a local  church)and extended to public schools, business premises (markets) and parks within the community. The campaign aimed to increase awareness about Hepatitis B, promote prevention measures, engage the community, and offer testing opportunities.

<h1 style="font-size:20px;">.Activities:<h1>
<h1 style="font-size:20px;">-Training of Volunteers:<h1> A total of 30  volunteers were engaged and trained to carry out this campaign. Volunteers were drawn from  Nursing schools in Buea and kumba. These volunteers were also the first beneficiaries of the initiative; all benefited from a free hepatitis B, C, and HIV test.</h1>
<h1 style="font-size:20px;">-Sensitization Sessions:<h1> Conducted interactive sessions at churches, kumba main market and traveling  agencies they were all educated on Hepatitis B transmission, symptoms, and prevention methods. Utilized visual aids, brochures, and presentations to deliver key messages effectively. Facilitated discussions to address misconceptions and clarify doubts regarding the disease.
<h1 style="font-size:20px;">-Community Engagement:<h1> Organized outreach programs in public schools to educate students about Hepatitis B through age-appropriate materials and activities. Collaborated with local businesses to disseminate information to employees and customers through posters, flyers, and digital screens. Encouraged community members to participate actively by sharing personal stories, testimonials, and engaging in social media campaigns.
<h1 style="font-size:20px;">-Testing Campaign:<h1> Provided free Hepatitis B testing at designated locations within the community, including the church premises, schools, and participating businesses/ public spaces. Implemented confidential testing procedures to ensure privacy and encourage participation.  Offered counseling services to individuals undergoing testing to address concerns and provide information on follow-up steps.
<h1 style="font-size:20px;">.Outcomes:<h1> Increased Awareness: Raised awareness about Hepatitis B and its risk factors among community members, including students and employees. Disseminated accurate information to dispel myths and misconceptions about the disease.
<h1 style="font-size:20px;">.Community Engagement:<h1> Fostered community involvement and collaboration through active participation in awareness activities and events. Strengthened partnerships with local institutions, including schools and businesses, for future health initiatives.
<h1 style="font-size:20px;">.Testing Uptake:<h1> Encouraged individuals to take proactive steps towards their health by offering accessible testing services. Identified individuals at risk of Hepatitis B infection and connected them with appropriate healthcare resources for further management. 
<h1 style="font-size:20px;">.Challenges Faced:<h1> Resource Constraints: Limited resources, including funding and manpower, posed challenges in reaching a larger audience and sustaining the campaign long-term.
<h1 style="font-size:20px;">.Stigma and Fear:<h1> Overcoming stigma associated with Hepatitis B and addressing fears related to testing and diagnosis required targeted efforts and sensitivity. Recommendations for Future Campaigns:
<h1 style="font-size:20px;">.Sustainability Planning:<h1> Develop long-term strategies to sustain awareness efforts beyond the initial campaign period, including securing ongoing funding and resources.
<h1 style="font-size:20px;">.Targeted Outreach:<h1> Tailor outreach efforts to specific demographic groups within the community, considering factors such as age, occupation, and cultural background.
<h1 style="font-size:20px;">.Collaborative Partnerships:<h1> Strengthen partnerships with local healthcare providers, community organizations, and government agencies to leverage resources and expertise for comprehensive health promotion initiatives.
<h1 style="font-size:20px;">.Conclusion:<h1> The Hepatitis B awareness campaign successfully engaged the local community through sensitization, community engagement, and testing activities. By raising awareness, promoting prevention, and offering testing services, the campaign contributed to the overall health and well-being of community members. Continued efforts and collaboration will be essential in sustaining momentum and addressing Hepatitis B-related challenges effectively as the campaign is still ongoing  there is an urgent need for  donation for it's Continuity( purchase of testing materials and production of  sensitization materials) as we are still carrying out routine sensitization and  in our health facility, and neighboring communities.</p><br><br>
