                <h3>About hepatitis</h3>
                <p> Hepatitis B is a vaccine-preventable liver infection caused by the hepatitis B virus (HBV). Hepatitis B is spread when blood, semen, or other body fluids from a person infected with the virus enters the body of someone who is not infected. This can happen through sexual contact; sharing needles, syringes, or other drug-injection equipment; or during pregnancy or delivery. Not all people newly infected with HBV have symptoms, but for those that do, symptoms can include fatigue, poor appetite, stomach pain, nausea, and jaundice. For many people, hepatitis B is a short-term illness. For others, it can become a long-term, chronic infection that can lead to serious, even life-threatening health issues like liver disease or liver cancer. Age plays a role in whether hepatitis B will become chronic. The younger a person is when infected with the hepatitis B virus, the greater the chance of developing chronic infection. About 9 in 10 infants who become infected go on to develop life-long, chronic infection. The risk goes down as a child gets older. About one in three children who get infected before age 6 will develop chronic hepatitis B. By contrast, almost all children 6 years and older and adults infected with the hepatitis B virus recover completely and do not develop chronic infection.

The best way to prevent hepatitis B is to get vaccinated. All adults aged 18-59 should receive the vaccine and any adult who requests it may get the vaccine.   All adults 18 years and older should get screened at least once in their lifetime.</p>
